package academia.equipamentos;


public class DadosEquipamento {
  private String nome;
  private String descricao;
  private String tipo;
  private String marca;
  private String idetificador;
  private String TesteCommit;



  public String getNome() {
    return nome;
  }
  public void setNome(String nome) {
    this.nome = nome;
  }
  public String getDescricao() {
    return descricao;
  }
  public void setDescricao(String descricao) {
    this.descricao = descricao;
  }
  public String getTipo() {
    return tipo;
  }
  public void setTipo(String tipo) {
    this.tipo = tipo;
  }
  public String getMarca() {
    return marca;
  }
  public void setMarca(String marca) {
    this.marca = marca;
  }
  public String getIdetificador() {
    return idetificador;
  }
  public void setIdetificador(String idetificador) {
    this.idetificador = idetificador;
  }
  
}
